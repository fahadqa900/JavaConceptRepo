package com.metlife.formulae;

public class Area
{
    public  double areaofRectangle(double length,double width)
    {
        double area1=length*width;
        return area1;
    }
    //Area of Triangle:
    public double areaofTriangle(double base,double height)
    {
        double area2=base*height/2;
        return area2;
    }

}
